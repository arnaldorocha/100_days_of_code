# Procedural programming (Programação procedural), 
# onde estabelecemos procedimentos ou funções que fazem coisas particulares.
# Em que um procedimento leva a outro procedimento, e no final das contas 
# o computador trabalhando na maioria de cima para baixo e depois saltando para uma função como necessário
# A programação procedural é um dos primeiros paradigmas da programação

