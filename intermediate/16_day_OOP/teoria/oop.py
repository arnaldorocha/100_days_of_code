# ser trabalhadas por equipes separadas, pessoas separadas (modulos), e também cada uma dessas peças 
# se tornam reutilizáveis se precisarmos do mesmo funcionalidade no futuro. 
# Mas Orientado a Objetos, a programação leva o fato esse conceito ainda mais longe
# 
# Ou seja cada um tem sua função a ser exercido, como se estivesse gerindo um restaurante,
